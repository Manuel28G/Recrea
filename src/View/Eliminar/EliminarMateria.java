    @Override
    public void ActionSiguiente(ActionEvent e) {
        if(ce.EliminarMateria(materia))
             JOptionPane.showMessageDialog(this,Util.MENSAJE_MATERIA_BORRADA, Util.DIALOG_TITULO_MENSAJE, JOptionPane.INFORMATION_MESSAGE);
        else
            JOptionPane.showMessageDialog(this,Util.ERROR_OPERACION_FALLIDA, Util.DIALOG_TITULO_MENSAJE, JOptionPane.INFORMATION_MESSAGE);
       
        ActionSalir(e);
    }

